def _safe(name: str) -> str:
    return "".join(c if c not in '/\\:*?"<>|' else "_" for c in name).strip()


def gen() -> int:
    total = 0
    for cat, info in SCENARIOS.items():
        out_dir = BASE / cat
        out_dir.mkdir(parents=True, exist_ok=True)
        # 清旧，保证可重跑
        for old in out_dir.glob("*.md"):
            old.unlink()
        for i, (title, case) in enumerate(info["cases"], 1):
            steps_text = "\n".join(f"{j}. {s}" for j, s in enumerate(info["steps"], 1))
            md = (
                "---\n"
                f"category: {cat}\n"
                f"title: {title}\n"
                f"source: 社区调解样例库/{cat}\n"
                "---\n\n"
                f"# {title}\n\n"
                f"## 案情\n{case}\n\n"
                f"## 法条依据\n{info['legal']}\n\n"
                f"## 调解步骤\n{steps_text}\n"
            )
            (out_dir / f"{i:02d}-{_safe(title)}.md").write_text(md, encoding="utf-8")
            total += 1
    return total


if __name__ == "__main__":
    n = gen()
    print(f"已生成 {n} 篇样例语料到 {BASE}")
